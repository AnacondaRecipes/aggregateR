export CRAN_URL=https://cran.r-project.org
export CONDA_R=4.2.0

mkdir -p logs

DATE=`date -Iseconds`
SKEL_LIST="logs/skel_list.log"
SKEL_GOOD="logs/skel_good.log"
SKEL_FAIL="logs/skel_fail.log"
SKEL_FULL="logs/skel_full.log"


echo $DATE > $SKEL_LIST
echo $DATE > $SKEL_GOOD
echo $DATE > $SKEL_FAIL
echo $DATE > $SKEL_FULL


skelly_one () {

    echo "================================================================================" >> $SKEL_FULL
    echo "Skeleton for ${@}" >> $SKEL_FULL
    echo "================================================================================" >> $SKEL_FULL
    conda skeleton cran \
          --cran-url ${CRAN_URL} \
          --output-suffix=-feedstock/recipe \
          --recursive \
          --update-policy=merge-keep-build-num \
          --r-interp=r-base \
          --use-noarch-generic \
          ${@}  >> $SKEL_FULL 2>> $SKEL_FULL || return 1

    return 0

    # was: --allow-archived \
}


skelly_all () {

    for JUST_THE_ONE in ${@}; do
        DATE=`date`
        echo -n "Skelly for ${JUST_THE_ONE}..."
        if skelly_one $JUST_THE_ONE ; then
            echo  "good: $JUST_THE_ONE" >> $SKEL_LIST
            echo  "$DATE $JUST_THE_ONE" >> $SKEL_GOOD
            echo  " good!"
        else
            echo  "fail: $JUST_THE_ONE" >> $SKEL_LIST
            echo  "$DATE $JUST_THE_ONE" >> $SKEL_FAIL
            echo  " FAIL!"
        fi
    done

}



